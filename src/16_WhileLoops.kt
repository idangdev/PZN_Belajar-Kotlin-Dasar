/*

WHILE LOOPS

- While adalah salah satu perulangan lain yang ada di Kotlin
- While adalah salah satu perulangan yang sangat flexible, dimana kode while akan melakukan
- pengecekan kondisi, jika kondisi bernilai true, maka dia akan menjalankan blok while, dan terus
- diulangi sampai kondisi while bernilai false


 */


fun main() {

    var i = 0

    while (i < 10){
        println("Perulangan $i")
        i++
    }

    println("Selesai Perulangan")

}